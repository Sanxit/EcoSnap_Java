package ecosnap.model;

public class BookTable {

	private int id;
	private String city;
	private String shootType;
	private String preferredDate;
	private String alternateDate;
	private String fullname;
	private String email;
	private String phone;
	private String whatsapp;

	// ID
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	// City
	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	// Shoot Type
	public String getShootType() {
		return shootType;
	}

	public void setShootType(String shootType) {
		this.shootType = shootType;
	}

	// Preferred Date
	public String getPreferredDate() {
		return preferredDate;
	}

	public void setPreferredDate(String preferredDate) {
		this.preferredDate = preferredDate;
	}

	// Alternate Date
	public String getAlternateDate() {
		return alternateDate;
	}

	public void setAlternateDate(String alternateDate) {
		this.alternateDate = alternateDate;
	}

	// Fullname
	public String getFullname() {
		return fullname;
	}

	public void setFullname(String fullname) {
		this.fullname = fullname;
	}

	// Email
	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	// Phone
	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	// WhatsApp
	public String getWhatsapp() {
		return whatsapp;
	}

	public void setWhatsapp(String whatsapp) {
		this.whatsapp = whatsapp;
	}
}
